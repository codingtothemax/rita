        <h2 class="text-xl bg-indigo-600 text-white inline-block p-1 my-3">
            Für mich ist die Bildhauerei ein Abenteuer, eine <span class="font-bold">Entdeckungsreise</span>.
        </h2>

        <p>Ich wähle einen Stein aus. <br>
            Vor mir liegt ein Berg unbehauener Steine, aus dem Berg gebrochen, ausgewählt, von weit her transportiert,
            zu dem Künstler, mit dem ich jetzt arbeiten möchte.</p>

        <p>Ein Stein spricht mich an. <br>
            Er zeigt schon Spuren der Verwitterung, die mir gefallen. Die Natur schafft besondere Formen und Farbe. <br>
            Der Stein steht vor mir, schwer und hart. <br>
            Zeigt er mir schon eine Form, die ich hervorholen will?</p>

        <p>Habe ich eine Idee, was aus dem Stein werden könnte, mache ich eine Skizze, entscheide, wie der Stein stehen
            soll, was oben und unten ist.
            Mit Hammer und Meißel bearbeite ich den Stein, feste Schläge, der Stein ist hart, er leistet Widerstand.
            Unermüdlich weiter, Stunde um Stunde.
            Es entsteht eine grobe Form. So könnte die Form des Kopfes aussehen.</p>
        <p>Dann die Form des Gesichtes festlegen. Gibt es einen Hals, gibt es Haare, sind die Ohren sichtbar, ...
            Ich arbeite mit tausenden Hammerschlägen die Form des Gesichtes heraus, der Stein bestimmt mit. Ist er
            würfelförmig oder oval, er prägt die Form des Kopfes.
            Wo liegt die Nase, die Augen, der Mund?</p>
        <p>Die Nase muss sichtbar werden. Jetzt die Flächen der Wangen zurücksetzen, genügend Stein für Augen und Mund
            stehen lassen, dann wieder viele Hammerschläge. Jetzt vorsichtiger schlagen, nicht zu tief.
            Der Stein ist hart. Bei jedem Schlag springt ein kleines Stück ab.
            Grobe Strukturen für Nase, Mund, Augen sind herausgearbeitet. Wie sollen sie genau aussehen? Runde oder
            spitze Nase, kurz oder lang.
            Die Augen, welche Form, geöffnet, geschlossen, groß oder klein. <br>
            Der Mund, wie sehen Lippen aus, immer wieder schauen und fühlen.</p>

        <p>Das Herausarbeiten der Details ist ein Prozess, der Stein ist hart, gibt bei vorsichtigen Schlägen nicht viel
            her, langsam finde ich eine Form, die mich anspricht. Noch keine Details.
            Jetzt den Hals, das Kinn formen. Ohren, besser nicht, sind schwierig. <br>
            Wie sehen die Haare aus, welche Form, Scheitel, gerade Strähnen, Wellen, kurz, lang. <br>
            Haare schienen einfach, jetzt stelle ich fest, dass der größte Teil des Kopfes aus Haaren besteht. <br>
            Wieder viele Hammerschläge, der Meißel wird von der linken Hand geführt und Schlag auf Schlag, Linie nach
            Linie, langsam nimmt die Frisur die gewünschte Form an.</p>

        <p>Stunde um Stunde konzentriert auf den Stein, mit Hammer und Meißel eine Skulptur schaffen, ist meditativ.
            Alles, womit sich der Kopf sonst beschäftigt, ist ausblendet.
            Dann ist das Werk fertig.
            Glück und Zufriedenheit erfüllen mich.</p>

        <div class="flex items-center justify-center bg-indigo-600 text-white text-lg p-16 sm:p-24 sm:h-40 my-6">
