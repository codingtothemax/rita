        <p>Seit acht Jahren bearbeite ich Steine, jedesmal ist es eine neue Herausforderung, ein Abenteuer.
            Gelernt habe ich die Grundlagen in der Bildhauerwerkstatt von Benedikt Birckenbach. Ich war von 2012 bis
            2015 Teilnehmerin seiner Bildhauerklasse. Jede Woche trafen sich einige Kunstbegeisterte in seiner
            Werkstatt, um zu arbeiten. Es war spannend, die Entstehung der verschiedenen Arbeiten zu erleben. Dazwischen
            war Zeit für Gespräche über das entstehende Werk.
            Dort habe ich überwiegend Skulpturen aus Sandstein geschaffen. Eine der ersten war „Der lachende Mann aus
            der Südsee“.</p>

        <p>In den folgenden Jahren habe ich jedes Jahr an Workshops verschiedener Künstler teilgenommen, die 3 bis 4
            Tage dauerten. So habe ich mit unterschiedlichen Materialien gearbeitet, die die Künstler entsprechend ihrer
            Arbeitstechniken auswählten.</p>

        <p>2015 und 2016 habe ich in der Bildhauerwerkstatt von Dirk Wilhelm gelernt, Alabaster zu bearbeiten, habe
            abstrakte Skulpturen geschaffen. Dort sind auch der „Jüngling“ aus Serpentinstein und der „Engel“ aus
            Alabaster entstanden.</p>

        <p>Ein besonderes Erlebnis waren die Workshops mit Künstlern aus Simbabwe. Ihre Kunst heißt Shona Art und sie
            werden seit einigen Jahren von einer Galerie aus Witten unterstützt, um ihre Kunstwerke aus Simbabwe in
            Deutschland verkaufen und Workshops geben zu können. Sie bringen Serpentinstein mit aus Simbabwe und
            vermitteln ihre Arbeitsweise.
            Die beiden Skulpturen „Big Mama“ und „Afrikanische Frau“ habe ich dort gearbeitet. Das Besondere bei diesem
            Stein, dass man ihn sehr fein polieren kann und dadurch eine besondere Wirkung erzielt werden kann.</p>

        <p>Seit zwei Jahren arbeite ich im Atelier von Marco de Piazza. Dort ist der Hauptwerkstoff Ton. Der Umgang mit
            Ton ist viel flexibler als Stein. Ich kann aus einem Tonblock Skulpturen herausarbeiten, es ist aber auch
            möglich eine Figur aufzubauen.
            Die Form ist lange Zeit formbar, veränderbar, ich kann experimentieren und korrigieren.
            Ich kann Modelle für meine Steinskulpturen formen und so kann ich komplexere Formen schaffen.
            Der menschliche Körper ist mein Thema, Menschen in Bewegung oder mit einer aussagekräftigen Körperhaltung
            wie „Die junge Frau“, „Der Mann in Blau“ und „Die Frau im blauen Kleid“.
            „Der singende Mann“ nach einer Skulptur von Ernst Barlach hat mich wegen der Körperhaltung uns seiner
            Ausdruckskraft fasziniert, er strahlt Ruhe und Gelassenheit aus, vermittelt die Freude am Gesang. Die
            Originalskulptur aus Terrakotta ist mit Acrylfarben gestaltet.
            Von diesem Original wurde durch das Wachsausschmelzverfahren in der Bronzegießerei eine Bronze gegossen.</p>

